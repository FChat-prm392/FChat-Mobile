package fpt.edu.vn.fchat_mobile.models;

public class Friend {
    private String id;
    private String fullname;
    private String username;
    private String imageURL;

    public String getId() {
        return id;
    }

    public String getFullname() {
        return fullname;
    }

    public String getUsername() {
        return username;
    }

    public String getImageURL() {
        return imageURL;
    }
}
